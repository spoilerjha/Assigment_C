//WAP to print size of an int, a float, a char, and a double type variable.
#include<stdio.h>
int main()
{
    int a=sizeof(a);
    float b=sizeof(b);
    char c=sizeof(c);
    double d=sizeof(d);
    printf("size of int:%d\n",a);
    printf("size of float:%f\n",b);
    printf("size of char:%c\n",c);
    printf("size of double :%lf",d);
    return 0;
}



